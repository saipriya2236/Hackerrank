def breakingRecords(scores):
    # Write your code here
    mn=mx=scores[0]
    cmn=cmx=0
    for score in scores:
        if score<mn:
            mn=score
            cmn+=1
        if score>mx:
            mx=score
            cmx+=1
    return cmx,cmn

